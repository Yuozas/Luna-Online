#pragma once

#include "SHCrop.h"
#include "SHFarmObj.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//
class CSHGarden : public CSHFarmObj
{
public:
	//----------------------------------------------------------------------------------------------------------------
	typedef enum														// 농장 텃밭 관리 결과
	{
		GARDEN_EVENT_NOTHING = 0,										// ..아무 일도..
		GARDEN_EVENT_CROP,												// ..농작물
	} GARDEN_EVENT;

private:
	//----------------------------------------------------------------------------------------------------------------
	CSHCrop*		m_pcsCrop;											// 농작물
	int				m_nCropNum;											// 농작물 개수

public:
	//----------------------------------------------------------------------------------------------------------------
	CSHGarden();
	~CSHGarden();

	//----------------------------------------------------------------------------------------------------------------
	//					농작물 생성
	VOID				Create(int nCropNum);	

	//----------------------------------------------------------------------------------------------------------------
	//					농작물 얻기
	CSHCrop*			GetCrop(int nCropID)		{ return &m_pcsCrop[nCropID]; }
	//					농작물 개수 얻기
	int					GetCropNum()				{ return m_nCropNum; }

	//----------------------------------------------------------------------------------------------------------------
	//					메인 루프
	virtual VOID		MainLoop();																
};